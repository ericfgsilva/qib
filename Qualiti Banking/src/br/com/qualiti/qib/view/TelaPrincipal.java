    private void opcaoMenuConsultarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_opcaoMenuConsultarClienteActionPerformed
       try {
        
            String cpf = JOptionPane.showInputDialog(this, "Favor informe o CPF do Cliente:", "Consultar Cliente", JOptionPane.PLAIN_MESSAGE);
            if (cpf != null && !cpf.isEmpty()){
                Cliente cliente = Fachada.obterInstancia().procurarCliente(cpf);
                JOptionPane.showMessageDialog(this, cliente);
            }
            
        } catch (ClienteNaoCadastradoException ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
    }//GEN-LAST:event_opcaoMenuConsultarClienteActionPerformed

